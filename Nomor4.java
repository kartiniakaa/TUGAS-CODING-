public class Nomor4 {
    public static void main(String[] args) {
        // Mendeklarasikan array dengan tipe data String
        String[] Olahraga = {"Badminton", "Volly", "Basket", "Sepak bola", "Tenis"};

        // Menampilkan semua nilai dalam array menggunakan perulangan for
        System.out.println("Nilai dalam Olahraga adalah:");
        for (int i = 0; i < Olahraga.length; i++) {
            System.out.println(Olahraga[i]);
        }
    }
}